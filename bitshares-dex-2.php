            <h2>Decentralized means direct</h2>
            <p>The decentralized exchange (further denoted simply as the DEX) allows for direct exchange of digital goods traded in the BitShares ecosystem. A decentralized exchange has a very particular set of advantages over traditional centralized exchanges, some highlights of which are below. Although the BitShares DEX comes with all of these features and many more, it is up to the token holders and users to leverage them in full or only partially.</p>

    <br />
            <h2>Separation of Powers:</h2>
            <h3>Refers to the division of responsibilities into distinct branches.</h3>
            <p><em>There is no reason why the same entity needs to be responsible for issuing IOUs and for processing the order book. In BitShares, order matching is performed by the protocol, which is unaware of implications concerning the involved assets.</em></p><br />
    <br />
            <h2>Global Unified Order Book:</h2>
            <h3>The BitShares Blockchain provides the logic and backend to keep one global ledger for all.</h3>
            <p><em>Since BitShares is global, an Internet connection is all that is required for anyone to use the DEX for trading, bringing the world’s liquidity to a single decentralized order book.</em></p>
    <br />
            <h2>Transactions:</h2>
            <h3>Any user interacting with the DEX is doing so via the Blockchain, authentic and authorized.</h3>
            <p><em>Transactions are constructed then transmitted to the network, containing instructions the user wants to perform. The most simple form is the transfer operation, which contains sender, receiver, amount, optionally with an encrypted memo.</em></p><br />
    <br />
            <h2>Trade Almost Anything:</h2>
            <h3>The BitShares DEX is asset agnostic.</h3>
            <p><em>On the BitShares DEX users can trade any pair, although some pairs may end up with low liquidity, such as SILVER:GOLD, other pairs such as USD:EUR for FOREX trading will see larger volume.</em></p>
    <br />
            <h2>Decentralized:</h2>
            <h3>Run across the world simultaneously on many nodes.</h3>
            <p><em>The DEX is decentralized across the globe. This not only means that there is no single point of failure, but it also implies that the BitShares exchange is open for trading 24/7 because it’s always daytime somewhere.</em></p>
    <br />  
            <h2>Scalable:</h2><br />
            <h3>Capable of 100,000 tp/s (transactions per second) with the network to go with it.</h3>
            <p><em>Delegated Proof of Stake ensures the BitShares network can confirm transactions in an average of just 1 second, limited only by the speed of light. See <a href='/technology/industrial-performance-scalability'>Industrial Performance and Scalability</a> for more</em></p>
            
    <br />
            <h2>Secure:</h2>
            <h3>Human-readable account names that must be registered together with public-keys in the blockchain.</h3>
            <p><em>Your funds and trades are secured with industry-grade elliptic curve cryptography. No one will be able to access your funds unless you let them. To further strengthen the security, users are able to setup escrow and multi-signature schemes.</em></p>
